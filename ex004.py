
a = input('Escereva algo. ')
print('O tipo primitivo desse valor é', type(a))
print('Só tem espaços?', a.isspace())
print('É um numeral?', a.isnumeric())
print('É alfabético?', a.isalpha())
print('É alfanumério?', a.isalnum())
print('Está em maiúsculos?', a.isupper())
print('Está em minúsculos', a.islower())
print('Está capitalizado',a.istitle())


